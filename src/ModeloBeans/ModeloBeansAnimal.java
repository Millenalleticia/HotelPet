
package ModeloBeans;


public class ModeloBeansAnimal {
    private  int codigo;
    private String nome;
    private  int data_nascimento;
    private String temperamento;
    private int peso; 
    private String raca;

    /**
     * @return the codigo
     */
    public int getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the data_nascimento
     */
    public int getData_nascimento() {
        return data_nascimento;
    }

    /**
     * @param data_nascimento the data_nascimento to set
     */
    public void setData_nascimento(int data_nascimento) {
        this.data_nascimento = data_nascimento;
    }

    /**
     * @return the temperamento
     */
    public String getTemperamento() {
        return temperamento;
    }

    /**
     * @param temperamento the temperamento to set
     */
    public void setTemperamento(String temperamento) {
        this.temperamento = temperamento;
    }

    /**
     * @return the peso
     */
    public int getPeso() {
        return peso;
    }

    /**
     * @param peso the peso to set
     */
    public void setPeso(int peso) {
        this.peso = peso;
    }

    /**
     * @return the raca
     */
    public String getRaca() {
        return raca;
    }

    /**
     * @param raca the raca to set
     */
    public void setRaca(String raca) {
        this.raca = raca;
    }

    

    
   
}
